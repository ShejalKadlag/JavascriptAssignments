
function roundToNearestWholeNumber(num) {
    return Math.round(num);
}

console.log(roundToNearestWholeNumber(3.7));
console.log(roundToNearestWholeNumber(4.9));

//Math.ceil() rounds a number up to the nearest whole number
console.log(Math.ceil(4.7));
console.log(Math.ceil(3.9));

//Math.floor() rounds a number down to the nearest whole number
console.log(Math.floor(5.7));
console.log(Math.floor(4.3));